                        <h3 class="mb-3"><strong>Deskripsi</strong></h3>
                        <p>Paskibraka adalah singkatan dari Pasukan Pengibar Bendera Pusaka dengan tugas utamanya mengibarkan duplikat bendera pusaka dalam upacara peringatanproklamasi kemerdekaan Indonesia di 3 tempat, yakni tingkat Kabupaten/Kota (Kantor Bupati/Walikota), Provinsi (Kantor Gubernur), dan Nasional (Istana Negara).
Anggotanya berasal dari pelajar SLTA Sederajat kelas 1 ATAU 2. Penyeleksian anggotanya biasanya dilakukan sekitar bulan April untuk persiapan pengibaran pada17 Agustus.<br>
Sejarah Gagasan Paskibraka lahir pada tahun 1946, pada saat ibukota Indonesia dipindahkan ke Yogyakarta. Memperingati HUT Proklamasi Kemerdekaan RI yang ke-1, Presiden Soekarno memerintahkan salah satu ajudannya, Mayor (Laut) Husein Mutahar, untuk menyiapkan pengibaran bendera pusaka di halaman Istana Gedung Agung Yogyakarta. Pada saat itulah, di benak Mutahar terlintas suatu gagasan bahwa sebaiknya pengibaran bendera pusaka dilakukan oleh para pemuda dari seluruh penjuru Tanah Air, karena mereka adalah generasi penerus perjuangan bangsa yang bertugas.

Tetapi, karena gagasan itu tidak mungkin terlaksana, maka Mutahar hanya bisa menghadirkan lima orang pemuda (3 putra dan 2 putri) yang berasal dari berbagai daerah dan kebertulan sedang berada di Yogyakarta. Lima orang tersebut melambangkan Pancasila.

Ketika Ibu kota dikembalikan ke Jakarta pada tahun 1950, Mutahar tidak lagi menangani pengibaran bendera pusaka. Pengibaran bendera pusaka pada setiap 17 Agustus di Istana Merdeka dilaksanakan oleh Rumah Tangga Kepresidenan sampai tahun 1966. Selama periode itu, para pengibar bendera diambil dari para pelajar dan mahasiswa yang ada di Jakarta

Dengan ide dasar dari pelaksanaan tahun 1946 di Yogyakarta, Husein Muntahar kemudian mengembangkan lagi formasi pengibaran menjadi 3 kelompok yang dinamai sesuai jumlah anggotanya, yaitu:<br>

- Kelompok 17 / pengiring (pemandu),<br>
- Kelompok 8 / pembawa (inti),<br>
- Kelompok 45 / pengawal.</p>

