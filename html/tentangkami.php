                        <h3 class="mb-4"><strong>SEJARAH</strong></h3>
                        <p>Sekolah Menengah Kejuruan ( SMK ) PELITA Bandung  didirikan pada tahun 1998 dengan surat ijin pendirian sekolah dari KANWIL DEPDIKNAS  Provinsi Jawa Barat dengan nomor : 1988/I02.1/Kep/OT/1998.  yang berdiri diatas lahan seluas 1404 M2 dengan status Hak Milik yang berada  dibawah naungan Yayasan Pengembangan Pendidikan Nasional ( YPPN ) dengan akte Notaris Nomor : 10 tanggal 6 oktober 1983. Pada awal Peneriman Siswa Baru yaitu tahun ajaran 1998 – 1999  SMK Pelita Bandung mampu menerima siswa angkatan pertama sebanyak 69 siswa, selanjutnya sejalan dengan tingkat kemajuan sekolah  serta beberapa prestasi yang diraih baik oleh siswa maupun guru, maka  prestise SMK Pelita Bandung mengalami perubahan yang cukup signifikan sehingga berdampak terhadap   kuantitas siswa yang menunjukan peningkatan yang sangat tinggi. Dengan prestasi dan prestise yang diraih begitu pesat oleh seluruh komponen SMK Pelita Bandung tersebut, maka pemerintah kota bandung memberikan perhatian khusus pada sekolah ini dan merubah status Terdaftar menjadi “DIAKUI “, hal ini berdasarkan keputusan Kepala Dinas Pendidikan Kota Bandung Nomor ; 379/I291-Dikmenjur/2002 tanggal 29 April 2002. Mengingat animo siswa dari tahun ketahun lulusan SMP/ Tsanawiyah yang melanjutkan ke SMK Pelita Bandung semakin tinggi, sehingga dibutuhkan sarana dan prasarana yang sesuai dengan standar pendidikan di sekolah kejuruan,  maka pihak yayasan melakukan perluasan lahan  dengan membeli tanah masyarakat yang lokasinya berdampingan dengan tanah yang sudah ada milik yayasan, sehingga luas tanah sekarang yang digunakan untuk proses pendidikan di SMK Pelita ini menjadi 4600 M2 dengan luas bangunan ± 3400 M2 yang terdiri dari 1 Ruang Kepala Sekolah, 1 Ruang Wakil Kepala Sekolah, 1 Ruang Staf Tata Usaha, 1 Ruang Guru, 1 Ruang Serba guna, 23 Ruang Belajar Teori, 1 Ruang Praktek Bahasa ( Audio-video visual ) 2 Ruang Praktek Komputer, 1 Ruang Praktek Perkantoran, 1 Ruang Praktek Penjualan, 1 Ruang Praktek Bank, 1 Ruang Praktek Perpustakaan, 1 Ruang BP/BK, 1 ruang kesehatan, 1 Ruang Mushola, 1 Ruang OSIS, 1 Ruang Studio Musik / Band, 1 Ruang Siaran Radio Pelita, dan ruang praktek  lainnya yang sesuai dengan kebutuhan siswa,  serta dilengkapi dengan kantin, lapang Basket, Volley, Bulu Tangkis, dan Futsal. Sebagai bukti keprofesionalan sekolah dalam mengelola pendidikan, maka pemerintah Provinsi Jawa Barat, melalui Badan Akreditasi Provinsi Sekolah telah memberikan lisensi terakreditasi  kepada SMK Pelita Bandung   dengan peringkat  AKREDITASI  “ A “ ( AMAT BAIK ), hal ini sesuai dengan Surat Keputusan BAP-S/M Nomor : 02.00/141/BAP-SM/XII/2007 tanggal 13 Desember 2007, hingga kini SMK Pelita Bandung Memiliki 1013 siswa yang terbagi dalam empat Program Keahlian atau Jurusan yaitu      Teknologi    Informatika     ( Komputer ),     Administrasi  Perkantoran  ( Sekretaris ),Penjualan  /  Pemasaran     ( Marketing ) dan otomotif ( Sepeda Motor ).</p>
  
