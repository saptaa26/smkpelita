                        <p>Pengertian Pramuka dan Sejarah Pramuka | Apa itu pramuka? Pramuka adalah singkatan dari Praja Muda Karana dan merupakan organisasi atau gerakan kepanduan. Pramuka adalah sebuah organisasi yang merupakan wadah proses pendidikan kepramukaan yang dilaksanakan di Indonesia. Dalam dunia internasional, Pramuka disebut dengan istilah “Kepanduan” (Boy Scout). Gerakan Pramuka memiliki kode Kode Kehormatan Pramuka, sebagaimana yang tertuang dalam Anggaran Dasar Pramuka, Gerakan Pramuka memiliki Kode Kehormatan yang terdiri atas janji yang disebut Satya dan Ketentuan Moral yang disebut Darma Kode Kehormatan Pramuka disesuaikan dengan golongan usia dan perkembangan rohani dan jasmaninya, yaitu:
Kode Kehormatan Pramuka Siaga terdiri atas Dwisatya dan Dwidarma.
Kode Kehormatan Pramuka Penggalang terdiri atas Trisatya Pramuka Penggalang dan Dasadarma.
Kode Kehormatan Pramuka Penegak dan Pandega terdiri atas Trisatya Pramuka Penegak dan Pramuka Pandega dan Dasadarma.
Kode Kehormatan Pramuka Dewasa terdiri atas Trisatya Anggota Dewasa dan Dasadarma.
Sejarah Pramuka dunia pertama kali dipelopori oleh Lord Baden Powell atau nama lengkapnya Robert Sthepenson Smyth Baden Powell of Giwell, seorang warga negara Inggris yang pernah menjadi tentara. Sejak kecil Baden Powell dikenal sebagai anak yang mencintai kegiatan luar ruangan (outdoor). Ia sering bermain di hutan kecil, di samping sekolahnya. Kemah pertama kepanduan yang dipimpin Baden Powell, terjadi pada tanggal 1 Agustus 1907 yang bertempat di Brownsea Island, Inggris. Karena itulah, Tanggal 1 Agustus pun ditetapkan sebagai Hari Kepanduan Dunia.</p>

  
